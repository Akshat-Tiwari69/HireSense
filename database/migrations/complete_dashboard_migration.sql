-- Step 6: ADMIN AUDIT LOG TABLE
-- ============================================================================
CREATE TABLE IF NOT EXISTS admin_audit_log (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    admin_id INTEGER NOT NULL,
    action_type TEXT NOT NULL,
    entity_type TEXT NOT NULL,
    entity_id INTEGER,
    old_values TEXT,
    new_values TEXT,
    description TEXT,
    timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (admin_id) REFERENCES users(id) ON DELETE SET NULL
);

CREATE INDEX IF NOT EXISTS idx_audit_log_admin ON admin_audit_log(admin_id);
CREATE INDEX IF NOT EXISTS idx_audit_log_timestamp ON admin_audit_log(timestamp);
CREATE INDEX IF NOT EXISTS idx_audit_log_action ON admin_audit_log(action_type);

-- Step 7: INTERVIEWER PREFERENCES TABLE
-- ============================================================================
CREATE TABLE IF NOT EXISTS interviewer_preferences (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    interviewer_id INTEGER NOT NULL UNIQUE,
    default_assessment_template_id INTEGER,
    email_notifications BOOLEAN DEFAULT true,
    assessment_time_window_minutes INTEGER DEFAULT 30,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (interviewer_id) REFERENCES users(id) ON DELETE CASCADE,
    FOREIGN KEY (default_assessment_template_id) REFERENCES assessment_templates(id) ON DELETE SET NULL
);

CREATE INDEX IF NOT EXISTS idx_interviewer_prefs ON interviewer_preferences(interviewer_id);

-- Step 8: ASSESSMENTS TABLE ENHANCEMENTS
-- ============================================================================
ALTER TABLE assessments
ADD COLUMN IF NOT EXISTS ai_recommendation TEXT,
ADD COLUMN IF NOT EXISTS ai_confidence_score REAL,
ADD COLUMN IF NOT EXISTS skill_gap_analysis TEXT,
ADD COLUMN IF NOT EXISTS interviewer_feedback TEXT,
ADD COLUMN IF NOT EXISTS final_decision_by_id INTEGER,
ADD COLUMN IF NOT EXISTS final_decision_date TIMESTAMP,
ADD CONSTRAINT fk_assessment_final_decision FOREIGN KEY (final_decision_by_id) REFERENCES users(id) ON DELETE SET NULL;

-- Step 9: PROCTORING ENHANCEMENTS
-- ============================================================================
ALTER TABLE proctoring_events
ADD COLUMN IF NOT EXISTS is_reviewed BOOLEAN DEFAULT false,
ADD COLUMN IF NOT EXISTS violation_acknowledged BOOLEAN DEFAULT false,
ADD COLUMN IF NOT EXISTS reviewer_id INTEGER,
ADD COLUMN IF NOT EXISTS reviewer_notes TEXT,
ADD CONSTRAINT fk_proctoring_reviewer FOREIGN KEY (reviewer_id) REFERENCES users(id) ON DELETE SET NULL;

-- Step 10: SCHEDULED ASSESSMENTS ENHANCEMENTS
-- ============================================================================
ALTER TABLE scheduled_assessments
ADD COLUMN IF NOT EXISTS job_id INTEGER,
ADD COLUMN IF NOT EXISTS proctor_id INTEGER,
ADD COLUMN IF NOT EXISTS proctor_notes TEXT,
ADD COLUMN IF NOT EXISTS proctoring_quality_score REAL,
ADD CONSTRAINT fk_scheduled_job FOREIGN KEY (job_id) REFERENCES job_descriptions(id) ON DELETE SET NULL,
ADD CONSTRAINT fk_scheduled_proctor FOREIGN KEY (proctor_id) REFERENCES users(id) ON DELETE SET NULL;

CREATE INDEX IF NOT EXISTS idx_scheduled_job ON scheduled_assessments(job_id);
CREATE INDEX IF NOT EXISTS idx_scheduled_proctor ON scheduled_assessments(proctor_id);

-- Step 11: ASSESSMENT ANALYTICS TABLE
-- ============================================================================
CREATE TABLE IF NOT EXISTS assessment_analytics (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    job_id INTEGER NOT NULL,
    assessment_date DATE NOT NULL,
    total_assessments_completed INTEGER DEFAULT 0,
    average_technical_score REAL,
    average_psychometric_score REAL,
    average_overall_score REAL,
    pass_rate REAL,
    avg_completion_time_minutes INTEGER,
    total_violations_recorded INTEGER,
    hired_count INTEGER DEFAULT 0,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (job_id) REFERENCES job_descriptions(id) ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS idx_analytics_job ON assessment_analytics(job_id);
CREATE INDEX IF NOT EXISTS idx_analytics_date ON assessment_analytics(assessment_date);
CREATE INDEX IF NOT EXISTS idx_analytics_job_date ON assessment_analytics(job_id, assessment_date);

