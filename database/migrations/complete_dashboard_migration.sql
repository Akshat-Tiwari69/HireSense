-- Step 1: ENHANCED JOB DESCRIPTIONS TABLE
-- ============================================================================
ALTER TABLE job_descriptions
ADD COLUMN IF NOT EXISTS created_by_id INTEGER,
ADD COLUMN IF NOT EXISTS ai_refined_description TEXT,
ADD COLUMN IF NOT EXISTS ideal_candidate_profile TEXT,
ADD COLUMN IF NOT EXISTS role_complexity_level TEXT DEFAULT 'intermediate',
ADD COLUMN IF NOT EXISTS skill_taxonomy TEXT,
ADD COLUMN IF NOT EXISTS status TEXT DEFAULT 'active',
ADD COLUMN IF NOT EXISTS is_archived BOOLEAN DEFAULT false,
ADD COLUMN IF NOT EXISTS last_modified_by_id INTEGER,
ADD CONSTRAINT fk_job_created_by FOREIGN KEY (created_by_id) REFERENCES users(id) ON DELETE SET NULL,
ADD CONSTRAINT fk_job_modified_by FOREIGN KEY (last_modified_by_id) REFERENCES users(id) ON DELETE SET NULL;

-- Step 2: JOB SCORING PROFILES TABLE
-- ============================================================================
CREATE TABLE IF NOT EXISTS job_scoring_profiles (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    job_id INTEGER NOT NULL UNIQUE,
    technical_weight REAL DEFAULT 0.7,
    psychometric_weight REAL DEFAULT 0.3,
    min_passing_score REAL DEFAULT 70.0,
    skill_thresholds TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (job_id) REFERENCES job_descriptions(id) ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS idx_job_scoring_profiles ON job_scoring_profiles(job_id);

-- Step 3: QUESTION BANK TABLE
-- ============================================================================
CREATE TABLE IF NOT EXISTS question_bank (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    question_text TEXT NOT NULL,
    question_type TEXT NOT NULL,
    category TEXT NOT NULL,
    difficulty TEXT DEFAULT 'intermediate',
    required_skills TEXT,
    options TEXT,
    correct_answer TEXT,
    code_starter TEXT,
    test_cases TEXT,
    expected_solution TEXT,
    trait TEXT,
    job_specific_keywords TEXT,
    is_active BOOLEAN DEFAULT true,
    created_by_id INTEGER,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (created_by_id) REFERENCES users(id) ON DELETE SET NULL
);

CREATE INDEX IF NOT EXISTS idx_question_bank_type ON question_bank(question_type);
CREATE INDEX IF NOT EXISTS idx_question_bank_category ON question_bank(category);
CREATE INDEX IF NOT EXISTS idx_question_bank_difficulty ON question_bank(difficulty);

-- Step 4: EMAIL TEMPLATES TABLE
-- ============================================================================
CREATE TABLE IF NOT EXISTS email_templates (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    template_name TEXT NOT NULL UNIQUE,
    subject TEXT NOT NULL,
    body TEXT NOT NULL,
    variables TEXT,
    is_default BOOLEAN DEFAULT false,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

